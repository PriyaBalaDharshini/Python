# 1. lower() - Converts string to lowercase
var="Hello World"
print(var.lower())

# 2. upper() - Converts string to uppercase
var="Hello World"
print(var.upper())

# 3. upper() - Capitalizes first character
var="hello world from priya"
print(var.capitalize())

# 4. title() - Capitalizes first letter of each word
var="hello world from priya"
print(var.title())

# 5. strip() - 	Removes leading/trailing spaces
word=" Hi "
print(word.strip())

